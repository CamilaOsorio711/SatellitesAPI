package com.example.satelites.Model;

/**
 * @author Camila Osorio
 * Clase que representa la posicion x y de la fuente que emite el mensaje
 */
public class Position {

    private double x;
    private double y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
